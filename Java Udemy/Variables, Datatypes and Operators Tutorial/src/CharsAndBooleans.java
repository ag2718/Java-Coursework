public class CharsAndBooleans {
    public static void main(String[] args) {

        //Characters
        char myChar = 'A'; //Can only be one character, width of 16
        char unicodeChar = '\u00AE'; //Many different characters, can be defined with special unicode
        System.out.println(myChar);
        System.out.println(unicodeChar);

        //Booleans
        boolean myBoolean = true; //Either true or false

    }
}
